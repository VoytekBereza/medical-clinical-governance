    <p><strong>1. Introduction</strong>
        <br /> Any extension to professional roles with regard the supply of medicines must take into account the need to protect patient safety, ensure continuity of care and safeguard patient choice and convenience. It also has to be cost-effective and bring demonstrable benefits to patient care.</p>

    <p>This patient group direction (PGD) will increase patient care in the area of travel medicine as there will be a specialised travel medicine professional handling all consultations. This PGD has been created with the aim of providing an efficient and effective Travel Health Services to Patients.</p>

    <p>This PGD is designed to guide pharmacists on the supply of doxycycline to patients requiring malaria prophylaxis for travel purposes. Doxycycline is a member of the tetracycline antibiotics group and is commonly used to treat a variety of infections. Doxycycline is a semi-synthetic tetracycline invented and clinically developed in the early 1960s. Doxycycline is an alternative to Malarone (atovaquone/proguanil) or Lariam (mefloquine) for the prophylaxis of malaria.</p>

    <p>Four steps remain essential to prevent damage to health due to malaria in UK travellers:</p>

    <ul>
        <li><strong>A</strong>wareness: know about the risk of malaria.</li>
        <li><strong>B</strong>ites by mosquitoes: prevent or avoid.</li>
        <li><strong>C</strong>ompliance with appropriate <strong>C</strong>hemoprophylaxis.</li>
        <li><strong>D</strong>iagnose breakthrough malaria swiftly and obtain treatment promptly.</li>
    </ul>

    <p>Recent guidelines give greater emphasis to the importance of balancing the risk of malaria and the risk of adverse reactions to antimalarials.</p>

    <p>PGDs do not remove inherent professional obligations or accountability. It is the responsibility of each professional to practice only within the bounds of their own competence.</p>

    <p>This PGD relates to two counterpart documents entitled:</p>

    <ul>
        <li>"Travel Risk Assessment Form" termed "tRAF" <span>throughout the </span>https://hubnet.io <span> website.</span></li>
        <li>“Malaria Risk Assessment Form" termed “mRAF" throughout the https://hubnet.io website.</li>
    </ul>

