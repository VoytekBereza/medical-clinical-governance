        <p><strong>4. Description of treatment available under this direction</strong></p>

        <p><strong>4.1 Doxycycline capsule</strong></p>

        <p>Each Doxycycline capsule for oral administration contains either doxycycline 50mg or 100mg.</p>

        <p><span style="text-decoration: underline;"><strong>Doxycycline is a Prescription-only Medicines (PoM). </strong></span></p>

        <p> </p>

        <p><strong>4.2 Dose, route and frequency</strong></p>

        <p>Prophylaxis of malaria:</p>

        <p>Prophylaxis should commence 24 or 48 hours prior to entering a malaria- endemic area and continue during the period of the stay. Prophylaxis should continue for 28 days after leaving the area.</p>

        <p><strong>Dosage in Adults</strong></p>

        <p>One 100mg Doxycycline or two 50mg Doxycycline capsules daily.</p>

        <p><strong>Dosage in children between the ages of 12 and 16</strong></p>

        <p>This is the same dose as with adults (see above).</p>

        <p>These dosages are correct as of December 2011 please check the BNF for more up to date information.</p>

        <p> </p>

        <p><strong>4.3 Duration of treatment available under this PGD</strong></p>

        <p>Doxycycline has been shown to be safe for use for up to 2 years. Therefore, under the terms of this PGD the maximum duration of treatment is 24 months.</p>

        <p> </p>

        <p><strong>4.4 Adverse effects</strong></p>

        <p>Nausea and stomach pain are the most frequent complaints. In rare case itching, rash and allergic reactions may be seen.</p>

        <p>Doxycycline increases the sensitivity to sunlight and may trigger a so-called photo-allergic dermatitis on areas of the skin exposed to sunlight or artificial sunlight. This reaction may be seen up to a week after the last capsule of doxycycline. The frequency of photo allergic reactions are probably around 1 per 300 users for the most serious to about 3% for mild reactions. Prescription of doxycycline should be followed by advise on protection against sunlight.</p>

        <p>See the BNF for more information.</p>

        <p><strong>Treatment of overdose</strong></p>

        <p>The patient should be referred for medical attention immediately.</p>

        <p> </p>

        <p><strong>4.5 Advice to patient</strong></p>

        <p>The patient should also be advised that it is important to take the capsules at the same time each day, to complete the full course and to take the capsule with a full glass of water whilst standing up.</p>

        <p>In the event of vomiting within 1 hour of dosing, a repeat dose should be taken and the course continued as before. The patient should obtain more doxycycline capsules to replace those that were vomited.</p>

        <p>The patient information leaflet contained in the medicine(s) should be made accessible to the patient/parent/guardian. Where this is unavailable, or unsuitable, sufficient information should be given to the patient/parent/guardian in a language that they can understand.</p>

        <p> </p>

        <p><strong>4.6 Follow up treatment</strong></p>

        <p>Advise patient if the patient develops a fever upon returning that they should consult their GP as this may be a possible indication of malaria.</p>

