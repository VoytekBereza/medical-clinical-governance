        <p><strong>2.6 Precautions</strong></p>

        <p>No antimalarial drug provides 100% protection even if used strictly according to the recommendations. Malaria drugs reduce the risk of infection with around 80% to 95% depending on destination and living conditions. Prevention of mosquito bites is therefore still important even if the patient takes the antimalarial drugs regularly. Travellers should be told about the importance of avoiding mosquito bites, the importance of taking prophylaxis regularly and the importance of an immediate visit to their doctor if they fall ill within one year and especially within three months.</p>

        <p>Photosensitivity manifested by an exaggerated sunburn reaction has been observed in some individuals taking tetracyclines, including doxycycline. Patients likely to be exposed to direct sunlight or ultraviolet light should be advised that this reaction can occur with tetracycline drugs and treatment should be discontinued at the first evidence of skin erythema. However, the patient should be informed that this may be transient and mild.</p>

        <p>Use in patients with impaired hepatic function- Doxycycline should be administered with caution to patients with hepatic impairment or those receiving potentially hepatotoxic drugs. Abnormal hepatic function has been reported rarely and has been caused by both the oral and parenteral administration of tetracyclines, including doxycycline.</p>

        <p>Excretion of doxycycline by the kidney is about 40% every 72 hours in individuals with normal renal function. This percentage excretion may fall to a range as low as 1-5% every 72 hours in individuals with severe renal insufficiency (creatinine clearance below 10ml/min). Studies have shown no significant difference in the serum half-life of doxycycline in individuals with normal and severely impaired renal function. Haemodialysis does not alter the serum half-life of doxycycline. The anti-anabolic action of the tetracyclines may cause an increase in blood urea. Studies to date indicate that this anti-anabolic effect does not occur with the use of doxycycline in patients with impaired renal function.</p>

        <p> </p>

        <p><strong>2.7 Concurrent medication</strong></p>

        <p>Check the latest BNF for the most up to date information on drug interactions, below is a summary of the most established doxycycline-drug interactions.</p>

        <p>The patient should understand to inform the pharmacist via the Travel information form if they take other capsules also non-prescription drugs. The efficacy of doxycycline is reduced if taken together with drugs containing calcium, magnesia or iron. These are found in antacids (against ulcer) drugs against constipation, vitamins and food supplements. Allow at least 3 hours between use of any of the above drugs and the intake of doxycycline.</p>

        <p>Phenobarbital, carbamazepine, primidone and phenytoin may increase the metabolism of doxycycline (reduced half-life). An increase in the daily dosage of doxycycline should be considered, the patient should be referred to receive further advice. Doxycycline may increase the plasma concentration of Ciclosporin. Co-administration should only be undertaken with appropriate monitoring, the patient should be referred to receive further advice.</p>

        <p><strong>Combined Oral Contraceptives</strong></p>

        <p>Latest recommendations state that there no additional contraceptive precautions are required when combined oral contraceptives are used with antibacterials that do not induce liver enzymes (ie doxycycline), unless diarrhoea or vomiting do occur. These recommendations should be discussed with the woman, who should be advised that information in the patient information leaflets may differ.</p>

