    <p><strong>2.3 Exclusion criteria</strong></p>

    <ul>
        <li>Children <strong>under 18 years old.</strong></li>
        <li>Weight <strong>under 45 kg.</strong></li>
        <li><strong>Breast feeding.</strong></li>
        <li><strong>Pregnancy.</strong></li>
        <li><strong>Epileptic</strong> or <strong>psychiatric</strong> <strong>disorders</strong>. Mainly, active depression, a history of depression, generalised anxiety disorder, psychosis, suicide attempts, suicidal ideations and self-endangering behaviour, schizophrenia or other psychiatric disorders, or with a history of convulsions of any origin.</li>
        <li>Mefloquine is contraindicated in individuals with known hypersensitivity to mefloquine or any component of the formulation. See BNF for details.</li>
        <li>Occupations requiring <strong>fine coordination</strong> or activities in which vertigo may be life-threatening, such as flying an aircraft.</li>
        <li><strong>Concurrent use</strong> of chloroquine or quinine-like drugs (<strong>halofantrine</strong> and mefloquine should not be used concurrently).</li>
        <li><span style="font-family:times new roman,times,serif;"><span style="font-size: 12px;">halofantrine must not be used during mefloquine chemoprophylaxis or treatment of malaria or within 15 weeks after the last dose of mefloquine, due to the risk of a potentially fatal prolongation of the QTc interval.</span></span>
        </li>
        <li>Underlying <strong>cardiac conduction disturbances</strong>.</li>
        <li>Patients with rare hereditary problems of <strong>galactose</strong> <strong>intolerance</strong>, the Lapp lactase deficiency or glucose-galactose malabsorption should not take this medicine.</li>
        <li>Patients with <strong>hepatic</strong> or <strong>renal</strong> impairment. Prophylactic use in patients with severe impairment of liver function should be regarded for the time being as a contraindication as no experience has been gained in such patients.</li>
        <li>Patients with a history of <strong>Blackwater fever</strong>.</li>
        <li>Aeroplane <strong>pilots</strong>.</li>
        <li><strong>Absence of valid consent.</strong></li>
    </ul>

