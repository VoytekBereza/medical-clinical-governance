    <p><strong>1. Introduction</strong></p>

    <p>Any extension to professional roles with regard to administration and supply of medicines must take into account the need to protect patient safety, ensure continuity of care and safeguard patient choice and convenience. It also has to be cost-effective and bring demonstrable benefits to patient care.</p>

    <p>This patient group direction (PGD) will increase patient care in the area of travel medicine as there will be a specialised travel medicine professional handling all consultations. This PGD has been created with the aim of providing an efficient, effective, accessible travel health service to patients.</p>

    <p>This PGD is designed to guide pharmacists on the supply of mefloquine (Lariam) to patients requiring malaria prophylaxis for travel purposes. Mefloquine is an alternative to Malarone (atovaquone/proguanil) or Doxycycline for the prophylaxis of malaria.</p>

    <p>Four steps remain essential to prevent damage to health due to malaria in UK travellers:</p>

    <ul>
        <li><strong>A</strong>wareness: know about the risk of malaria.</li>
        <li><strong>B</strong>ites by mosquitoes: prevent or avoid.</li>
        <li><strong>C</strong>ompliance with appropriate <strong>C</strong>hemoprophylaxis.</li>
        <li><strong>D</strong>iagnose breakthrough malaria swiftly and obtain treatment promptly. </li>
    </ul>

    <p>Recent guidelines give greater emphasis to the importance of balancing the risk of malaria and the risk of adverse reactions to antimalarials.</p>

    <p>PGDs do not remove inherent professional obligations or accountability. It is the responsibility of each professional to practice only within the bounds of their own competence.</p>

