    <p><strong>2.6 Precautions and special warnings</strong></p>

    <p>No antimalarial drug provides 100% protection even if used strictly according to the recommendations. Malaria drugs reduce the risk of infection with around 80% to 95% depending on your destination and living conditions. Travellers should be told about the importance of avoiding mosquito bites, the importance of taking prophylaxis regularly and the importance of an immediate visit to their doctor if they fall ill within one year and especially within three months.</p>

    <p><strong>Pregnancy</strong> - the manufacturer advises that adequate contraception during prophylaxis and for three months after stopping (teratogenicity in animal studies).</p>

    <p>Mefloquine is considered not to be the drug of choice for SCUBA holidays.</p>

    <p style="font-family: Times;"><strong>Interaction with vaccines</strong></p>

    <p style="font-family: Times;">When mefloquine is taken concurrently with oral live typhoid vaccines, attenuation of immunization cannot be excluded. Vaccinations with attenuated live bacteria should therefore, be completed at least 3 days before the first dose of mefloquine.</p>

    <p><strong style="font-family: Times;">Neuropsychiatric Adverse Reactions</strong></p>

    <p>Mefloquine may induce psychiatric symptoms such as anxiety disorders, paranoia, depression, hallucinations and psychosis. Psychiatric symptoms such as nightmares, acute anxiety, depression, restlessness or confusion have to be regarded as prodromal for a more serious event. Cases of suicide, suicidal thoughts and self-endangering behaviour such as attempted suicide have been reported.</p>

    <p><span style="font-family: Times; font-size: 12px;">Cases of polyneuropathy </span><span style="font-family: Times;">(based on neurological</span><span style="font-family: Times; font-size: 12px;"> symptoms</span><span style="font-family: Times;"> s</span><span style="font-family: Times; font-size: 12px;">uch as pain, </span><span style="font-family: Times;">burning, sensory disturbances or muscle weakness, alone or in combination) have been reported in patients receiving mefloquine. Mefloquine should be discontinued in patients experiencing symptoms of neuropathy, including pain, burning, tingling, numbness, and/or weakness in order to prevent the development of an irreversible </span><span style="font-family: Times; font-size: 12px;">condition.</span></p>

    <p>Patients on malaria chemoprophylaxis with mefloquine should be informed that if these reactions or changes to their mental state occur during mefloquine use, to stop taking mefloquine and seek medical advice immediately so that mefloquine can be replaced by alternative malaria prevention medication.</p>

    <p>Due to the long half-life of mefloquine, adverse reactions may occur and persist up to several months after discontinuation of the drug. In a small number of patients it has been reported that dizziness or vertigo and loss of balance continued for months after discontinuation of the drug. To minimise the risk of these adverse reactions, mefloquine must not be used for chemoprophylaxis in patients with active or a history of psychiatric disturbances such as depression, anxiety disorders, schizophrenia or other psychiatric disorders.</p>

    <p><strong>Cardiac toxicity</strong></p>

    <p>Concomitant administration of mefloquine and other related compounds (e.g. quinine, quinidine and chloroquine) may produce electrocardiographic abnormalities. Due to the risk of a potentially fatal prolongation of the QTc interval, halofantrine must not be used during mefloquine chemoprophylaxis or treatment of malaria, or within 15 weeks after the last dose of mefloquine. Due to increased plasma concentrations and elimination half-life of mefloquine following co-administration with ketoconazole, the risk of QTc prolongation may also be expected if ketoconazole is taken during mefloquine chemoprophylaxis or treatment of malaria, or within 15 weeks after the last dose of mefloquine. Patients should be advised to consult a doctor, if signs of arrhythmia or palpitations occur during chemoprophylaxis with mefloquine. These symptoms might, in rare cases, precede severe cardiologic side effects.</p>

    <p style="font-family: Times;"><strong>Seizure disorders</strong></p>

    <p>Concomitant administration of mefloquine and anticonvulsants (e.g. valproic acid, carbamazepine, phenobarbital or phenytoin) may reduce seizure control by lowering the plasma levels of anticonvulsant. Therefore, patients concurrently taking anti-seizure medication, including valproic acid, carbamazepine, phenobarbital and phenytoin, and mefloquine should have the blood level of their anti-seizure medication monitored and the dosage adjusted as necessary. Concomitant administration of mefloquine and drugs known to lower the epileptogenic threshold (antidepressants such as tricyclic or selective serotonin reuptake inhibitors (SSRIs); bupropion; antipsychotics; tramadol; chloroquine or some antibiotics) may increase the risk of convulsions.</p>

    <p><b>Geographical drug resistance</b></p>

    <p><span style="font-size: 11px; color: rgb(0, 0, 0); font-family: Arial, Helvetica, sans-serif;">Geographical drug resistance patterns of P. falciparum occur and preferred choice of malaria chemoprophylaxis might be different from one area to another. Resistance of </span><i style="font-size: 11px; color: rgb(0, 0, 0); font-family: Arial, Helvetica, sans-serif;">P. falciparum</i><span style="font-size: 11px; color: rgb(0, 0, 0); font-family: Arial, Helvetica, sans-serif;"> to mefloquine has been reported, predominantly in areas of multi-drug resistance in South-East Asia. Cross-resistance between mefloquine and halofantrine and cross-resistance between mefloquine and quinine have been observed in some regions. For current advice on geographical resistance patterns competent national expert centres should be consulted.</span></p>

    <p><strong>Eye disorders</strong></p>

    <p>Any patient presenting with a visual disorder should be referred to a physician as certain conditions (such as retinal disorders or optic neuropathy) may require stopping treatment with mefloquine.</p>

    <p><strong>Impaired liver function</strong></p>

    <p>In patients with impaired liver function, the elimination of mefloquine may be prolonged, leading to higher plasma levels and a higher risk of adverse reactions.</p>

    <p><strong>Pneumonitis</strong></p>

    <p>Pneumonitis of possible allergic aetiology has been reported in patients receiving mefloquine. Patients who develop signs of dyspnoea, dry cough or fever etc. while receiving mefloquine should be advised to contact a doctor to undergo medical evaluation.</p>

    <p><strong>Inhibitors and Inducers of CYP3A4</strong></p>

    <p>Inhibitors and Inducers of the isoenzyme CYP3A4 may modify the pharmacokinetics/metabolism of mefloquine, leading to an increase or decrease in mefloquine plasma concentrations.</p>

    <p><strong style="font-family: Times;">Interaction with vaccines</strong></p>

    <p>When mefloquine is taken concurrently with oral live typhoid vaccines, attenuation of immunisation cannot be excluded. Vaccinations with oral attenuated live bacteria should therefore, be completed at least 3 days before the first dose of mefloquine.</p>

    <p style="font-family: Times;"><strong>Hypoglycaemia</strong></p>

    <p style="margin: 0px; font-family: Arial, Helvetica, sans-serif; padding: 4px 0px; color: rgb(0, 0, 0);"><span style="font-size:11px;">The possibility of hypoglycaemia in patients with congenital hyperinsulinaemic hypoglycaemia should be considered. Patients should not disregard the possibility that re-infection or recrudescence may occur after effective antimalarial therapy.</span></p>

    <p style="font-family: Times;"><b>Galactose intolerance </b></p>

    <p style="font-family: Times;"><span style="color: rgb(0, 0, 0); font-family: Arial, Helvetica, sans-serif; font-size: 11px;">Patients with rare hereditary problems of galactose intolerance, the Lapp lactase deficiency or glucose-galactose malabsorption should not take this medicine.</span></p>

    <p><strong>Long term use</strong></p>

    <p>During clinical trials, this drug was not administered for longer than one year. If the drug is to be administered for a prolonged period, periodic evaluations including liver function tests and periodic ophthalmic examinations should be performed.</p>

    <p> </p>

    <p><strong>2.7 Concurrent medication</strong></p>

    <p>The patient should understand to inform the pharmacist via the Travel information form if you take other medications also non-prescription drugs. Drug-drug interactions with mefloquine have not been explored in detail. There is one report of cardiopulmonary arrest, with full recovery, in a patient who was taking a beta blocker (propranolol). The effects of mefloquine on the compromised cardiovascular system have not been evaluated. The benefits of mefloquine therapy should be weighed against the possibility of adverse effects in patients with cardiac disease.</p>

    <p>Because of the danger of a potentially fatal prolongation of the QTc interval, halofantrine must not be given simultaneously with or subsequent to mefloquine.</p>

    <p>Concomitant administration of mefloquine and other related compounds (eg, quinine, quinidine and chloroquine) may produce electrocardiographic abnormalities and increase the risk of convulsions. If these drugs are to be used in the initial treatment of severe malaria, mefloquine administration should be delayed at least 12 hours after the last dose. There is evidence that the use of halofantrine after mefloquine causes a significant lengthening of the QTc interval. Clinically significant QTc prolongation has not been found with mefloquine alone.</p>

    <p>This appears to be the only clinically relevant interaction of this kind with mefloquine, although theoretically, coadministration of other drugs known to alter cardiac conduction (eg, anti-arrhythmic or beta-adrenergic blocking agents, calcium channel blockers, antihistamines or H1-blocking agents, tricyclic antidepressants and phenothiazines) might also contribute to a prolongation of the QTc interval. There are no data that conclusively establish whether the concomitant administration of mefloquine and the above listed agents has an effect on cardiac function.</p>

    <p>In patients taking an anticonvulsant (eg, valproic acid, carbamazepine, phenobarbital or phenytoin), the concomitant use of mefloquine may reduce seizure control by lowering the plasma levels of the anticonvulsant. Therefore, patients concurrently taking antiseizure medication and mefloquine should have the blood level of their antiseizure medication monitored and the dosage adjusted appropriately.</p>

    <p>When mefloquine is taken concurrently with oral live typhoid vaccines, attenuation of immunisation cannot be excluded. Vaccinations with attenuated live bacteria should therefore, be completed at least 3 days before the first dose of mefloquine.</p>

