        <br />
        <strong>2.1 Inclusion criteria</strong></p>

    <ul>
        <li>Patients above <strong>18 years old.</strong></li>
        <li>Weight <strong>above 45kg</strong>.</li>
        <li>Travellers going to malaria endemic areas of the world where there is chloroquine highly resistant falciparum malaria, who are at moderate to high risk of exposure. mefloquine is also used as an alternative to doxycycline or malarone.</li>
        <li>For current advice on geographical resistance patterns and appropriate chemoprophylaxis, current guidelines or the Malaria Reference Laboratory should be consulted, details of which can be found in the British National Formulary (BNF).</li>
        <li><strong>Completion of the tRAF/mRAF in conjunction with the tRAF/mRAF guidance for PGD authorised clinicians provided on https://hubnet.io/.</strong></li>
    </ul>

