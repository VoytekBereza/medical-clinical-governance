    <p><strong>4. Description of Treatment Available Under this Direction</strong></p>

    <p><strong>4.1 Mefloquine Tablets</strong></p>

    <p>Each mefloquine tablet for oral administration contains 250mg of mefloquine (please check specific pack for extra excipient such as lactose). The Branded white scored “Larium” additionally contains excipients: poloxamer, microcrystalline cellulose, lactose, maize starch, crospovidone, ammonium clacium alignate, talc and magnesium stearate.</p>

    <p><span style="text-decoration: underline;"><strong>Mefloquine is a Prescription-only Medicines (PoM).</strong></span></p>

    <p> </p>

    <p><strong>4.2 Dose, route and frequency</strong></p>

    <p>Prophylaxis of malaria:</p>

    <p>For malaria prophylaxis the stated dose of Lariam should be given once weekly, always on the same day. Treatment should be initiated at least one week and up to 2-3 weeks before arrival in a malarious area and continued for 4 weeks after leaving (minimum treatment period 6 weeks). The maximum recommended duration of administration of Lariam is 12 months.</p>

    <p> </p>

    <p><strong>4.3 Duration of treatment available under this PGD</strong></p>

    <p>Mefloquine is licensed to be used for up to 12 months therefore, under the terms of this PGD the maximum duration of treatment is 12 months.</p>

    <p> </p>

    <p><strong>4.4 Adverse effects</strong></p>

    <p>Mefloquine is generally well tolerated when used for chemoprophylaxis. Approximately 20% to 30% of travellers will experience side effects from either mefloquine or chloroquine; most of these are mild and self-limiting. The most frequent minor side effects reported with mefloquine use are nausea, strange dreams, dizziness, mood changes, insomnia, headache, and diarrhoea. Approximately 1% to 4% of mefloquine users discontinue prophylaxis because of adverse effects, a rate not significantly different from other chemoprophylaxis regimens.</p>

    <p>Severe neuropsychiatric reactions (psychosis, convulsions) are infrequent with prophylactic doses and are reported to occur in approximately 1/10,000 to 1/13,000 individuals. Less severe but nonetheless troublesome neuro-psychologic adverse events (anxiety, depression, nightmares, etc.) requiring drug discontinuation are reported in less than 1% of users. In treatment with higher doses (25 mg base/kg), mefloquine is less well tolerated and severe neuropsychiatric reactions are reported to be 10 to 60 times more frequent.</p>

    <p>Healthcare professionals are reminded that mefloquine is associated with potentially serious neuropsychiatric reactions and that it is contra-indicated for malaria prophylaxis in those with a history of psychiatric disorders or convulsions. Abnormal dreams, insomnia, anxiety, and depression occur commonly. Psychosis, suicidal ideation, and suicide have also been reported. Psychiatric symptoms such as nightmares, acute anxiety, depression, restlessness, or confusion should be regarded as potentially prodromal for a more serious event. If neuropsychiatric symptoms occur, patients should be advised to discontinue mefloquine and to seek immediate medical attention so that mefloquine can be replaced with an alternative antimalarial. Adverse reactions may occur and persist up to several months after discontinuation because mefloquine has a long half-life</p>

    <p>See the BNF for more information.</p>

    <p><strong>Treatment of overdose</strong></p>

    <p>The patient should be referred for medical attention immediately.</p>

    <p> </p>

    <p><strong>4.5 Advice to patient</strong></p>

    <p>The patient should also be advised that it is important to take the tablets at the same time each week and to complete the full course.</p>

    <p>In the event of vomiting within 1 hour of dosing, a repeat dose should be taken and the course continued as before. The patient should obtain more mefloquine tablets to replace those that were vomited.</p>

    <p>The patient information leaflet contained in the medicine(s) should be made accessible to the patient/parent/guardian. Where this is unavailable, or unsuitable, sufficient information should be given to the patient/parent/guardian in a language that they can understand.</p>

    <p>Caution should be exercised with regard to activities requiring alertness and fine motor coordination such as driving, piloting aircraft, operating machinery, as dizziness, vertigo or a loss of balance, or other disorders of the central or peripheral nervous system and psychiatric disorders have been reported during and following the use of mefloquine. These effects may occur after therapy is discontinued due to the long half-life of the drug. In a small number of patients dizziness or vertigo and loss of balance have been reported to continue for months after discontinuation of the drug.</p>

    <p> </p>

    <p><strong>4.6 Follow up treatment</strong></p>

    <p>Advise patient if the patient develops a fever upon returning that they should consult their GP as this may be a possible indication of malaria.</p>

