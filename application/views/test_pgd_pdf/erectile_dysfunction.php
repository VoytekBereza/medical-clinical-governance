    <p><strong>1. Introduction</strong>
        <br /> This document is a comprises three PGDs for sildenafil, vardenafil and tadalafil for the treatment of erectile dysfunction, which is the inability to achieve or maintain a penile erection sufficient for satisfactory sexual performance. These PGDs have been combined as these PDE-5 inhibitors share similar pharmacological action, differences have been extensively highlighted throughout this document.</p>

    <p>This patient group direction (PGD) is designed to guide healthcare professionals on the supply of sildenafil, vardenafil and tadalafil for erectile dysfunction. The purpose of this PGD is to enable a healthcare professional, working within a CQC registered business or GPhC registered pharmacy, who has received specific training and has been assessed as competent to administer medicines in accordance with the following PGD and recommendations issued by the Department of Health.</p>

    <p>It is the responsibility of individual healthcare professionals and their line manager to ensure that they work within the terms laid down in this PGD and to ensure that staff are working to the most up to date PGD. By doing so, the quality of the services offered will be maintained, and the chances of staff making erroneous decisions which may affect patient, staff or visitor safety and comfort will be reduced. Supervisory staff at all levels must ensure that staff using this PGD act within their own level of competence. PGDs do not remove inherent professional obligations or accountability. It is the responsibility of each professional to practice only within the bounds of their own competence.</p>

    <p>This PGD relates to a counterpart document entitled “Erectile Dysfunction Risk Assessment Form" termed “edRAF" throughout the https://hubnet.io website.</p>

    <p><span style="text-decoration: underline;"><strong>This PGD will be reviewed annually, by the authorising clinicians, to ensure that it reflects contemporary guidance.</strong></span>
        <br />
    </p>

    <p><strong>2. Clinical decision making</strong>
        <br />
        <strong>2.1 Inclusion criteria</strong></p>

    <ul>
        <li>Men between the age of <strong>18-70</strong>.</li>
        <li>Any patients who are <strong>not</strong> <strong>contraindicated</strong>.</li>
        <li>Men who have moderate erectile dysfunction as defined by the <strong>8-16 point SHIM score</strong> (see edRAF).</li>
        <li><strong>Completion of the edRAF in conjunction with the edRAF guidance for PGD authorised clinicians provided on https://hubnet.io.</strong></li>
    </ul>

    <p><strong>2.2 Patients who may receive the supply of either sildenafil, vardenafil or tadalafil</strong>
        <br /> All patients in 2.1 above, who do not want specifically to consult with a doctor and are willing to have treatment from the PGD authorised healthcare professional.</p>

    <p> </p>

    <p><strong>2.3 Exclusion criteria</strong></p>

    <ul>
        <li><strong>Female</strong> patients.</li>
        <li>Men who have <strong>severe</strong> or <strong>mild</strong> erectile dysfunction as defined by scoring outside the 8-16 point SHIM score range (see edRAF).</li>
        <li>Patients <strong>outside</strong> the <strong>18-70 age</strong> range may not be supplied with erectile dysfunction medicines under this PGD refer them to their doctor.</li>
        <li>Sildenafil, vardenafil and tadalafil are contraindicated in individuals with known <strong>hypersensitivity</strong> to sildenafil, vardenafil and tadalafil or any component of their formulation. See the current BNF for details.</li>
        <li>Use of other <strong>co-cominant treatments</strong> for erectile dysfunction medicines contraindicates patients for treatment under this PGD.</li>
        <li><strong>Absence of valid consent.</strong></li>
    </ul>

    <p>Patients who suffer from any of the following conditions are also excluded:</p>

    <ul>
        <li>Anatomical <strong>deformation of the penis</strong> (eg angulation, cavernosal fibrosis, Peyronie’s disease)</li>
        <li>Pre-disposition to <strong>priapism</strong> (eg sickle cell disease, multiple myeloma or leukaemia)</li>
        <li>Excessive <strong>bleeding</strong> <strong>disorders</strong> (such as haemophilia)</li>
        <li>Stomach <strong>ulceration </strong></li>
        <li>History of <strong>uncontrolled blood pressure</strong> (BP)</li>
        <li><strong>Hypotensive</strong> patients with a systolic BP below <strong>90mmHg or diastolic below 70mmHg</strong>, if the patient is unsure the pharmacist may offer a BP check</li>
        <li>Hypertensive patients with a systolic BP above <strong>160mmHg or diastolic above 120mmHg</strong>, if the patient is unsure the pharmacist may offer a BP check</li>
        <li>Recent <strong>stroke</strong></li>
        <li>Unstable <strong>angina </strong></li>
        <li><strong>Myocardial infarction </strong></li>
        <li>Left <strong>ventricular outflow obstruction </strong></li>
        <li>Where <strong>vasodilation</strong> or <strong>sexual activity are inadvisable</strong> (i.e for cardiovascular risk factors etc)</li>
        <li>Previous history of non-arteric anterior ischaemic optic <strong>neuropathy </strong></li>
        <li><strong>Hepatic</strong> or <strong>renal</strong> <strong>impairment</strong> (decreased liver/kidney function)</li>
        <li>Hereditary degenerative <strong>retinal</strong> <strong>disorders</strong> (including genetic disorders of retinal phosphodiesterases)</li>
    </ul>

    <p> </p>

    <p><strong>2.4 Actions for patients who do not wish to receive care under this PGD</strong></p>

    <ul>
        <li>The patient should be advised of the risks of pregnancy.</li>
        <li>Document advice given.</li>
        <li>Document refusal in notes.</li>
        <li>Seek medical advice if necessary.</li>
    </ul>

    <p> </p>

    <p><strong>2.5 Precautions</strong></p>

    <p>Caution should be taken if the patient is suffering from cardiovascular disease. If the patient suffers from any of the following side effects they should be referred to a doctor as soon as possible:</p>

    <ul>
        <li>Loss of vision or hearing</li>
        <li>Chest pain with nausea and discomfort</li>
        <li>Continuous painful erection lasting 4 or more hours</li>
    </ul>

    <p>Before supplying erectile dysfunction medicines to patients who are eligible to receive them on the NHS, the pharmacist should ensure that the patient is aware of this fact. Conditions which may make the patient eligible include: multiple sclerosis, Parkinson’s disease, polio, prostate cancer, severe pelvic or spinal injury, spina bifida certain genetic conditions, such as Huntington's disease. Patients can also get ED medication on the NHS if they have had any of the following treatments: pelvic surgery, surgical removal of the prostate gland (prostatectomy), dialysis for kidney failure and kidney transplant.</p>

    <p><strong>N.B. Dizziness and altered vision were reported in clinical trials with sildenafil, patients should be aware of how they may react before driving or operating machinery.</strong></p>

    <p> </p>

    <p><strong>2.6 Concurrent medication</strong></p>

    <p>Drug-drug interactions with PDE-5 inhibitors have not been explored in detail. However, some known interactions do exist. Below is a brief summary of these, however, for the latest information all pharmacists authorised under this PGD should consult the most current BNF.</p>

    <p>Use of other co-cominant treatments for erectile dysfunction medicines (eg. alprostadil) contraindicates patients for treatment under this PGD. PDE-5 inhibitors are contraindicated in patients receiving the following medicines:</p>

    <ul>
        <li><strong>Medicinal nitrates</strong> (including oral, transdermal and sublingual)</li>
        <li><strong>Recreational nitrates</strong> such as amyl nitrate (poppers)</li>
        <li><strong>Alpha-Blockers</strong> (unless stabilised for more than 2 months)</li>
        <li><strong>Anti-virals</strong> - mainly protease inhibitors. Atazanavir, etravirine, fosamprenavir, indinavir, nelfinavir, ritonavir, saquinavir.</li>
        <li><strong>Nicorandil </strong></li>
        <li>Combination of <strong>tadalafil</strong> and <strong>doxazosin</strong>.</li>
        <li>Combinations of <strong>vardenafil</strong> and treatments for <strong>arrhythmias</strong>, (eg quinidine, procainamide...)</li>
    </ul>

    <p><span style="text-decoration: underline;"><strong>Alpha Blockers</strong></span>: If the patient has been stabilised on an alpha blocker for more than 2 months there is a slight increase in the risk of hypotension. This may prove clinically useful however if this arises it is best to take half of the initial dose.</p>

    <p><span style="text-decoration: underline;"><strong>Imidazole antifungals</strong></span>: Ketoconazole, imidazole. Half the initial dose and then titrate up accordingly.</p>

    <p><span style="text-decoration: underline;"><strong>Macrolide antibiotics</strong></span>: Clarithromycin, erythromycin, telithromycin. Half the initial dose and then titrate up accordingly.</p>

