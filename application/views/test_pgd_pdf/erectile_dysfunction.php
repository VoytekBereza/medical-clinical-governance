            <p><strong>5.3 Consent </strong></p>

            <p>Prior to the supply of a medicine, consent must be obtained, preferably written, from the patient, and documented either in the patient’s medical records/notes or on an administration form.</p>

            <p>The key points about consent include:</p>

            <ul>
                <li>If a patient’s fitness and suitability cannot be established, supply should be deferred.</li>
                <li>There is no legal requirement for consent to be in writing but written consent serves to record the decision and the discussions that have taken place and that the patient or person with parental responsibility giving consent on a child’s behalf has been informed about the process, benefits and risks of supply.</li>
                <li>Consent - either written or verbal - is required at the time of each supply.</li>
                <li>Consent remains valid unless the individual who gave it withdraws it. If there is new information between the time consent was given and when the supply is offered, including new evidence of risk, new medicines becoming available or where there is a significant change in the individual’s condition, it may be necessary for the patient to reconfirm their consent.</li>
                <li>Written and verbal information should be available in a form that can be easily understood by the person who will be giving the consent. Where English is not easily understood, translations and properly recognised interpreters should be used in order that they can make informed consent.</li>
                <li>The bringing of a patient for the supply after an invitation to attend for this purpose may be viewed as acceptance that the patient may have the administration.</li>
                <li>Individuals (patient, parent, guardian or person with parental responsibility) should also be informed about how data on the supply will be stored, who will be able to access that information and how that data may be used.</li>
                <li>Where consent is either refused or withdrawn, this decision must be documented.</li>
                <li>Consent obtained before the occasion upon which a patient is brought for the supply is only an agreement for the patient to be included in the administration programme and does not mean that consent is in place for each future administration.</li>
            </ul>

