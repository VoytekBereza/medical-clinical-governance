    <p><strong>4. Description of treatment available under this direction</strong></p>

    <p><span style="text-decoration: underline;"><strong>4.1 Sildenafil tablets</strong></span></p>

    <p>Each Sildenafil tablet for oral administration contains either 25mg, 50mg or 100mg sildenafil (as citrate).</p>

    <p><span style="text-decoration: underline;"><strong>Sildenafil is a Prescription-only Medicines (PoM). </strong></span></p>

    <p> </p>

    <p><span style="text-decoration: underline;"><strong>4.2 Vardenafil tablets </strong></span></p>

    <p>Each vardenafil tablet for oral administration contains either 5mg, 10mg or 20mg vardenafil.</p>

    <p><span style="text-decoration: underline;"><strong>Vardenafil is a Prescription-only Medicines (PoM). </strong></span></p>

    <p> </p>

    <p><span style="text-decoration: underline;"><strong>4.3 Tadalafil tablets </strong></span></p>

    <p>Each tadalafil tablet for oral administration contains either 2.5mg, 5mg, 10mg, 20mg tadalafil.</p>

    <p><span style="text-decoration: underline;"><strong>Tadalafil is a Prescription-only Medicines (PoM).</strong></span></p>

    <p> </p>

    <p><strong>4.2 Dose, route and frequency</strong></p>

    <p>All erectile dysfunction medicine presented in this PGD are exclusively for oral administration and should be taken about 60 minutes before their action is needed. Swallow tablets whole with water. Taking ED medication with food can delay the onset of action to 2 hours.</p>

    <p>Combinations of sildenafil citrate, vardenafil and tadalafil should not be taken in combination without a break of 36 hours between one medication and another.</p>

    <p><span style="text-decoration: underline;"><strong>Sildenafil </strong></span></p>

    <p>Adults (over 18 years of age) initially 50mg approximately 1 hour before sexual activity, subsequent doses should be adjusted according to the response to 25-100mg as a single dose as needed a maximum of one dose in 24 hours (max.single dose 100mg). Suggested pharmacy label wording:"One tablet approximately one hour before sexual activity. Maximum of one tablet in 24 hours."</p>

    <p><strong><span style="text-decoration: underline;">Vardenafil </span></strong></p>

    <p>Adults (over 18 years of age) initially 10mg approximately 25-60 minutes before sexual intercourse (vardenafil has been shown to act quicker than other erectile dysfunction medicines), subsequent doses adjusted according to initial response up to a maximum of 20mg as a single dose. Maximum one dose in 24 hours. Suggested pharmacy label wording:"One tablet approximately 25-60 minutes before sexual activity. Maximum of one tablet in 24 hours."</p>

    <p><span style="text-decoration: underline;"><strong>Tadalafil </strong></span></p>

    <p>Adults (over 18 years of age) initially 10mg approximately 30 minutes before sexual activity, subsequent doses adjusted according to response, up to 20mg as a single dose. Maximum 1 dose in 24 hours (but daily dose of 10-20mg not recommended). Suggested pharmacy label wording:"One tablet approximately 30 minutes before sexual activity. Maximum of one tablet in 48 hours."</p>

    <p>Men who need regular ED medication twice or more a week have the option to take low dose tadalafil either 2.5mg or 5mg tablets once daily on a continuous basis. The daily dose is low; however, tadalafil stays in the body for 36 hours and builds up to effective levels when taken daily. The dose instruction for low dose daily tadalafil 2.5mg or 5mg is for one tablet to be taken daily around the same time each day.</p>

    <p> </p>

    <p><strong>4.5 Choice of either sildenafil, vardenafil or tadalafil </strong></p>

    <p>Initiation of ED treatment usually starts with supply of <span style="text-decoration: underline;"><strong>sildenafil</strong></span> as it has the longest history of clinical data. In the event that the patient requests another medicine the patients motivation and contraindications should be discussed. Sildenafil has the shortest half-life (4 hours) in comparison to tadalafil (36 hours) and vardenafil (12 hours). <span style="text-decoration: underline;"><strong>Vardenafil</strong></span> is the fastest acting and the effect should be present within 30 minutes of taking the tablet. <span style="text-decoration: underline;"><strong>Tadalafil</strong></span> is most suited to patients who expect to continue sexual activity for a prolonged period.</p>

    <p> </p>

    <p><strong>4.6 Duration of treatment available under this PGD </strong></p>

    <p>In respects to frequency of the use of erectile dysfunction medicines, it is unusual in that there appears to be no clinical reason to restrict the number of tablets (up to one a day) prescribed to an individual in any given period, however, quantity is restricted on this PGD. Quantity of tablets and repeats:</p>

    <ul>
        <li><strong>Sildenafil / Vardenafil</strong>: Supply 4 or 8 tablets. Repeat no more frequently than at 1 or 2 week intervals respectively.</li>
        <li><strong>Tadalafil</strong>: Supply 4 or 8 tablets. Repeat no more frequently than at 2 or 4 week intervals respectively.</li>
        <li><strong>Tadalafil (2.5mg/5mg)</strong>: Patients taking regular daily Cialis must inform their GPs, particularly where existing or new medication is being prescribed. Can be repeated for continuous daily use. Supply 28 tablets initially. Repeat no more frequently than at 1 or 2 month intervals.</li>
    </ul>

    <p>In general, it is good to note that the average frequency of sexual intercourse in the 40 - 60 age range is once a week.</p>

    <p> </p>

    <p><strong>4.7 Adverse effects </strong></p>

    <p><span style="text-decoration: underline;"><strong>Sildenafil </strong></span></p>

    <p>The most common side effects of sildenafil include dyspepsia, nausea, vomiting, headache (including migraine) flushing, dizziness, myalgia, back pain, visual disturbances (non-arteritic anterior ischaemic optic neuropathy has been reported - stop drug if sudden visual impairment occurs) and nasal congestion.</p>

    <p><span style="text-decoration: underline;"><strong>Vardenafil </strong></span></p>

    <p>The most common side effects include: dizziness; flushing; headache; heartburn; nausea; stuffy or runny nose; upset stomach. At the 20 mg dose Levitra film-coated tablets, elderly ( 65 years old) patients had higher frequencies of headaches (16.2% versus 11.8%) and dizziness (3.7% versus 0.7%) than younger patients (
        < 65 years old). In general, the incidence of adverse reactions (especially “dizziness”) has been shown to be slightly higher in patients with a history of hypertension.</p>

            <p><span style="text-decoration: underline;"><strong>Tadalafil</strong></span></p>

            <p>The most common side effects include: cough; dizziness; flushing; headache; heartburn; mild back or muscle pain; stomach upset; stuffy or runny nose. See the most current BNF for more information. Treatment of overdose Patient should be referred for medical attention immediately.</p>

            <p> </p>

            <p><strong>4.8 Advice to patient </strong></p>

            <p>Experience to date suggests sildenafil can safely be prescribed throughout life provided that this is within the terms of the summary of product characteristics in the marketing authorisation (which specifies contraindications and a starting age of 18). However, the frequency of contra-indications increases with age, and SMAC is particularly concerned that prolonged prescribing could mask the progression of serious underlying disease. Some psychological causes of erectile dysfunction resolve spontaneously or with treatment, so the need for and tolerance of sildenafil should be reassessed at 1-3 months. Thereafter the appropriateness of prescribing should normally be checked yearly, but stopped immediately if interacting drugs have to be prescribed for other conditions.</p>

