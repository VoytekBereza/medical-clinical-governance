    <p><strong>2.6 Precautions</strong></p>

    <p>The safety and effectiveness of Atovaquone 62.5mg & Proguanil Hydrochloride 25mg for prophylaxis of malaria in children who weigh less than 11 kg has not been established.</p>

    <p>Persons taking Atovaquone & Proguanil Hydrochloride for prophylaxis of malaria should take a repeat dose if they vomit within 1 hour of dosing. In the event of diarrhoea, normal dosing should be continued. Absorption of atovaquone may be reduced in patients with diarrhoea or vomiting, but diarrhoea or vomiting is not associated with reduced efficacy. However, as with other antimalarial agents, subjects with diarrhoea or vomiting should be advised to continue to comply with personal protection measures (repellants, bednets).</p>

    <p>No antimalarial drug provides 100% protection even if used strictly according to the recommendations. Malaria drugs reduce the risk of infection with around 80% to 95% depending on your destination and living conditions. Travellers should be told about the importance of avoiding mosquito bites, the importance of taking prophylaxis regularly and the importance of an immediate visit to their doctor if they fall ill within one year and especially within three months.</p>

    <p>Occasionally, severe allergic reactions (including anaphylaxis) have been reported in patients taking Atovaquone & Proguanil Hydrochloride. If patients experience an allergic reaction, Atovaquone & Proguanil Hydrochloride should be discontinued promptly and appropriate treatment initiated.</p>

    <p> </p>

    <p><strong>2.7 Concurrent medication</strong></p>

    <p>Check the latest BNF for the most up to date information on drug interactions, below is a summary of the most established Atovaquone & Proguanil Hydrochloride interactions.</p>

    <p>Proguanil may potentiate the anticoagulant effect of warfarin and other coumarin based anticoagulants. Caution is advised when initiating or withdrawing malaria prophylaxis in patients on continuous treatment with coumarin based anticoagulants.</p>

    <p>Concomitant treatment with metoclopramide and tetracycline have been associated with a significant decrease in plasma concentration of atovaquone.</p>

    <p>Concomitant administration of atovaquone and indinavir results in a decrease in the Cmin of indinavir (23% decrease; 90% CI 8-35%). Caution should be exercised when prescribing atovaquone with indinavir due to the decrease in the trough levels of indinavir.</p>

    <p>Concomitant administration of rifampicin or rifabutin is known to reduce atovaquone levels by approximately 50% and 34%, respectively and is not recommended.</p>

    <p>Atovaquone is highly protein bound (> 99%) but does not displace other highly protein bound drugs in vitro, indicating significant drug interactions arising from displacement are unlikely.</p>

