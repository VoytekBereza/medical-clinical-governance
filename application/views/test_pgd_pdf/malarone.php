        <strong>2. Clinical Decision Making</strong>
        <br />
        <strong>2.1 Inclusion Criteria</strong></p>

    <ul>
        <li>Travellers going to malaria-endemic areas of the world where there is chloroquine or mefloquine resistant falciparum malaria, who are at moderate to high risk of exposure.</li>
        <li>Atovaquone with proguanil combinations are also used as an alternative to mefloquine or doxycycline.</li>
        <li><strong>Completion of the tRAF/mRAF in conjunction with the tRAF<strong>/mRAF</strong> guidance for PGD authorised clinicians provided on www.hubnet.io.</strong>
        </li>
    </ul>

    <p> </p>

    <p><strong>2.2 Patients who may receive the supply of Atovaquone & Proguanil Hydrochloride</strong>
        <br /> All patients in 2.1 above, who do not want specifically to consult with a doctor and are willing to have treatment from the PGD authorised healthcare professional.</p>

    <p> </p>

    <p><strong>2.3 Exclusion Criteria</strong></p>

    <ul>
        <li>Atovaquone 250mg & Proguanil Hydrochloride 100mg full strength tablets are not recommended for malaria prophylaxis in persons <strong>under 40 kg bodyweight</strong>. (Approximate weight of 12-year-old is 39 Kg) for this Atovaquone 62.5mg & Proguanil Hydrochloride 25mg must be used.</li>
        <li>Atovaquone 62.5mg & Proguanil Hydrochloride 25mg, should not be used in patients <strong>weighing less than 11Kg</strong>.</li>
        <li>Atovaquone & Proguanil Hydrochloride is contraindicated in individuals with known <strong>hypersensitivity</strong> to atovaquone or proguanil hydrochloride or any component of the formulation. See SPC for details.</li>
        <li>Atovaquone & Proguanil Hydrochloride is contraindicated for prophylaxis of P. falciparum malaria in patients with <strong>renal impairment</strong> (creatinine clearance
            < 30mL/min).</li>
                <li>Women who are <strong>pregnant</strong> or <strong>breast feeding</strong>, should not receive Atovaquone & Proguanil Hydrochloride.</li>
                <li>The patient is between the ages of 16-18 and not accompanied by a legal guardian unless the pharmacist has undergone a successful Enhanced Disclosure and Barring Service (DBS - previously called an enhanced CRB check)</li>
                <li>The patient is under the age of 16 and does not have an accompanying guardian who is legally authorised to consent on the child's behalf.</li>
                <li><strong>Absence of valid consent.</strong></li>
    </ul>

