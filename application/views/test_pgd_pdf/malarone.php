    <p><strong>4. Description of treatment available under this direction</strong></p>

    <p><strong>4.1 Atovaquone 250mg & Proguanil Hydrochloride 100mg & Atovaquone 62.5mg & Proguanil Hydrochloride 25mg tablets</strong></p>

    <p>Each <strong>adult tablet</strong> for oral administration contains atovaquone 250mg and proguanil hydrochloride 100mg as film-coated round, biconvex, pink tablets.</p>

    <p>Each <strong>paediatric tablet</strong> for oral administration contains atovaquone 62.5mg and proguanil hydrochloride 25mg as film-coated round, biconvex, pink tablets engraved 'GX CG7' on one side.</p>

    <p><span style="text-decoration: underline;"><strong>Atovaquone & Proguanil Hydrochloride tablets are Prescription-only Medicines (PoM).</strong></span></p>

    <p> </p>

    <p><strong>4.2 Dose, route and frequency</strong></p>

    <p>Prophylaxis of malaria:</p>

    <p>Prophylaxis should commence 24 or 48 hours prior to entering a malaria- endemic area and continue during the period of the stay. Prophylaxis should continue for 7 days after leaving the area.</p>

    <p><strong>Dosage in Adults</strong></p>

    <p>One Atovaquone & Proguanil Hydrochloride tablet daily.</p>

    <p>Atovaquone 250mg& Proguanil Hydrochloride 100mg tablets are not recommended for malaria prophylaxis in persons under 40kg bodyweight.</p>

    <p><strong>Dosage in children of Atovaquone 62.5mg & Proguanil Hydrochloride 25mg</strong></p>

    <p>The dosage for the prophylaxis of malaria in children is based on body weight. Prophylaxis Dosage in individuals weighing 11-40 kg.</p>

    <p>Child body weight 11-21 Kg: 1 Tablet once daily</p>

    <p>Child body weight 21-31 Kg: 2 Tablets once daily </p>

    <p>Child body weight 31-40 Kg: 3 Tablets once daily </p>

    <p>Child body weight over 40 Kg: Use adult dose</p>

    <p>The safety and effectiveness of Atovaquone 62.5mg & Proguanil Hydrochloride 25mg Tablets for prophylaxis of malaria in children who weigh less than 11 kg has not been established.</p>

    <p>These dosages are correct as of September 2015 please check the BNF for more up to date information.</p>

    <p> </p>

    <p><strong>4.3 Duration of treatment available under this PGD</strong></p>

    <p>Atovaquone & Proguanil Hydrochloride is licensed to be used for up to 93 days. Therefore, under the terms of this PGD the maximum duration of treatment is 93 days.</p>

    <p> </p>

    <p><strong>4.4 Adverse effects</strong></p>

    <p>In clinical trials for prophylaxis of malaria, the most commonly reported adverse events, independent of attributability, were headache, abdominal pain and diarrhoea, and were reported in a similar proportion of subjects receiving Atovaquone & Proguanil Hydrochloride or placebo.</p>

    <p> </p>

    <p><strong>Very common side effects (reported in more than 1 in 10 patients treated):</strong> </p>

    <ul>
        <li>Headache</li>
        <li>Feeling sick</li>
        <li>Stomach pain</li>
        <li>Diarrhoea</li>
    </ul>

    <p> </p>

    <p><strong>Common side effects (up to 1 in 10 people):</strong></p>

    <ul>
        <li>Dizziness</li>
        <li>Sleeping problems</li>
        <li>Strange dreams</li>
        <li>Depression</li>
        <li>Loss of appetite</li>
        <li>Fever</li>
        <li>Rash</li>
        <li>Cough</li>
    </ul>

    <p> </p>

    <p><strong>Uncommon side effects (up to 1 in 100):</strong> </p>

    <ul>
        <li>Anxiety</li>
        <li>An unusual awareness of abnormal beating of the heart</li>
        <li>Swelling and redness of the mouth</li>
        <li>Hair loss</li>
    </ul>

    <p> </p>

    <p><strong>Other side effects:</strong></p>

    <p>Anaemia and neutropenia are common and pancytopenia has been seen in patients with severe renal impairment.</p>

    <p>Adverse effects that have been reported include hyponatraemia, nausea, vomiting, gastric intolerance, oral ulceration, stomatitis, cough, insomnia, dizziness, fever.</p>

    <p>Allergic reactions including rash, urticaria and angioedema have been reported with isolated reports of anaphylaxis. Abnormalities in liver function tests were reversible and not associated with untoward clinical events.</p>

    <p><strong>Treatment of overdose</strong></p>

    <p>The patient should be referred for medical attention immediately.</p>

    <p><strong>Effects on ability to drive or operate machinery</strong></p>

    <p>Dizziness has been reported. Patients should be warned that if affected they should not drive, operate machinery or take part in activities where this may put themselves or others at risk.</p>

    <p> </p>

    <p><strong>4.5 Advice to patient</strong></p>

    <p>The patient should be advised if possible to take the tablets with food or a milky drink to ensure maximal absorption.</p>

    <p>The patient should also be advised that it is important to take the tablets at the same time each day and to complete the full course</p>

    <p>In the event of vomiting within 1 hour of dosing, a repeat dose should be taken and the course continued as before. The patient should obtain more Atovaquone 62.5mg & Proguanil Hydrochloride 25mg tablets to replace those that were vomited.</p>

    <p>The patient information leaflet contained in the medicine(s) should be made accessible to the patient/parent/guardian. Where this is unavailable, or unsuitable, sufficient information should be given to the patient/parent/guardian in a language that they can understand.</p>

    <p> </p>

    <p><strong>4.6 Follow up treatment</strong></p>

    <p>Advise patient if the patient develops a fever upon returning that they should consult their GP as this may be a possible indication of malaria.</p>

