    <p><strong>2.5 Actions for patients who do not wish to receive care under this PGD</strong></p>

    <ul>
        <li>The patient should be advised of the risks of malaria in the region being visited and given advice on bite avoidance and the signs and symptoms of malaria infection.</li>
        <li>Document advice given.</li>
        <li>Document refusal in notes.</li>
        <li>Seek medical advice if necessary</li>
    </ul>

